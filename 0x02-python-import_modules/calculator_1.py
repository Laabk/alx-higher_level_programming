#!/usr/bin/python3
def add(a, b):
    """the addition function to b imported
    the arguments involved:
        a: first integer passed
        b: second integer passed

    Returns: a + b
    """
    return (a + b)

def sub(a, b):
    return (a - b)

def mul(a, b):
    return (a * b)

def div(a, b):
    return int(a / b)

